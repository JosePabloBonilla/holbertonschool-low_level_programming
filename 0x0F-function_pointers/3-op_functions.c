#include "3-calc.h"

/**
 * op_add - sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of integers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product
 * @a: first integer
 * @b: second integer
 * Return: product of integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: first integer
 * @b: second integer
 * Return: division of integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: first integer
 * @b: second integer
 * Return: remainder of division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
